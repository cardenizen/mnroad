  CREATE OR REPLACE FORCE VIEW "MNR"."DISTRESS_DECODE_DATES_ML" ("CELL", "LANE", "AC", "RUTTING", "PATHWAYS", "PAVETECH", "FRICTION") AS 
  SELECT   cell, lane, MIN (DECODE (table_code, 'DISTRESS_AC', DAY)) ac,
            MIN (DECODE (table_code, 'DISTRESS_RUTTING_STRAIGHT_EDGE', DAY)
                ) rutting,
            MIN (DECODE (table_code, 'DISTRESS_PATHWAYS_VALUES', DAY)
                ) pathways,
            MIN (DECODE (table_code, 'DISTRESS_PAVETECH_VALUES', DAY)
                ) pavetech,
            MIN (DECODE (table_code, 'DISTRESS_FRICTION_DATA', DAY)) friction
