  CREATE OR REPLACE FORCE VIEW "MNR"."CR_VALUES_STATS" ("DAY", "CELL", "SEQ", "MAX_DAY", "MIN_DAY", "AVERAGE_DAY", "STD_DEV_DAY") AS 
  SELECT   DAY, cell, seq, ROUND (MAX (VALUE), 3) max_day,
            ROUND (MIN (VALUE), 3) min_day, ROUND (AVG (VALUE),
                                                   3) average_day,
            ROUND (STDDEV (VALUE), 3) std_dev_day
       FROM cr_values
   GROUP BY DAY, cell, seq
   ORDER BY DAY DESC, cell, seq 
 

  CREATE OR REPLACE FORCE VIEW "MNR"."DISTRESS_DATES_ML" ("DAY", "CELL", "LANE", "TABLE_CODE") AS 
  SELECT
DISTINCT DAY,
cell cell,
RTRIM (lane, ' ') lane,
'DISTRESS_AC' table_code
FROM distress_ac
WHERE RTRIM (lane, ' ') IN ('Driving', 'Passing')
UNION
