CREATE OR REPLACE VIEW LAYERS AS
SELECT
C.ID                        CELL_ID ,
SUBSTR(C.CLASS,24)          CELL_TYPE ,
C.CELL_NUMBER               CELL ,
C.CONSTRUCTION_ENDED_DATE ,
C.DEMOLISHED_DATE ,
C.DESIGN_LIFE ,
C.DRAINAGE_SYSTEM ,
C.CELL_END_STATION ,
C.END_STATION ,
C.NAME                      CELL_DESCRIPTION ,
C.ROAD_SECTION_ID ,
C.CELL_START_STATION ,
C.START_STATION ,
C.HMA_DESIGN ,
C.FIBER_TYPE ,
C.SURFACE_TEXTURE ,
C.SHOULDER_TYPE ,
C.MNDOT_MIX_SPECIFICATION ,
C.TIEBARS ,
C.LONGITUDINAL_JOINT_SEAL ,
LN.ID                       LANE_ID ,
LN.LANE_NUM ,
LN.NAME                     LANE ,
LN.WIDTH ,
LN.PANEL_LENGTH ,
LN.PANEL_WIDTH ,
LN.OFFSET_REF ,
L.ID                        LAYER_ID ,
L.CONSTRUCT_END_DATE ,
L.CONSTRUCT_START_DATE ,
L.LAYER_NUM ,
L.MATERIAL_ID ,
L.THICKNESS ,
L.DOWEL_BAR_LENGTH ,
L.DOWEL_BAR_DIAMETER ,
L.TRANSVERSE_STEEL
FROM MNR.CELL C 
 JOIN MNR.LANE LN ON LN.CELL_ID=C.ID 
 JOIN MNR.LAYER L ON L.LANE_ID=LN.ID
